// Ctrl Alt c for refactoring
const YEAR: i32 = 1993;

fn main() {
    let ten_years_ago = YEAR - 10;
    let five_years_ago = YEAR - 5;
    let one_year_ago = YEAR - 1;

    println!("Ten years ago: {}", ten_years_ago);
    println!("Five years ago: {}", five_years_ago);
    println!("One year ago: {}", one_year_ago);
}