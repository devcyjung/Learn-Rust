// main.rs cannot be called from integration test (tests directory)
// Therefore, use lib.rs and call lib.rs in main and tests, with "use".
pub fn add_two(a: i32) -> i32 {
    internal_adder(a, 2)
}

fn internal_adder(a: i32, b: i32) -> i32 {
    a + b
}

// Unit test goes in the same file as source code
#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn internal() {
        assert_eq!(4, internal_adder(2, 2));
    }
}
